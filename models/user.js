var mongoose = require('mongoose');

// Users Schema

var userSchema = mongoose.Schema({
    firstname:{
        type:String,
        required:true
    },
    lastname:{
        type:String,
        required:true
    },
    username:{
        type:String,
        required:true
    },
    password:{
        type:String,
        required:true
    }
});

var User = module.exports = mongoose.model('users',userSchema)